package edu.joao.Operadores;

public class Operadoresunarios {
    public static void main (String [] agrs) throws Exception{

        int numero = 5;

        System.out.println(numero);

        System.out.println (++numero);

        System.out.println( --numero);

        System.out.println (++numero);

        System.out.println (numero++);

        System.out.println (numero);

        // A uma diferença, se o ++ for antes da variável, o valor se altera no msm instante, se o ++ vir dps da variável, o valor se altera na proxima linha

    }
}
